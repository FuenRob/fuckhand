package main

import (
	"fmt"
	"github.com/spf13/cobra"
	"os"
)

func main() {
	var rootCmd = &cobra.Command{
		Use:   "middlefinger",
		Short: "Muestra una mano con el dedo medio levantado",
		Run: func(cmd *cobra.Command, args []string) {
			printMiddleFinger()
		},
	}

	if err := rootCmd.Execute(); err != nil {
		fmt.Println(err)
		os.Exit(1)
	}
}

